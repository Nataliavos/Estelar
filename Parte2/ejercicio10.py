numeroUno = int(input(' Ingresa Numero Uno ')) # recive datos
numeroDos = int(input(' Ingresa Numero Dos ')) # recive datos

suma = numeroUno + numeroDos # variable con formula de suma
resta = numeroUno - numeroDos# variable con formula de resta
multiplicacion = numeroUno * numeroDos # variable con formula de multiplicacion
divicion = numeroUno / numeroDos # variable con formula de division
print('--------------------SUMA---------------------------------')
print(f'La suma de ', numeroUno, ' y ' , numeroDos,' es: ', suma)
print('--------------------RESTA--------------------------------')
print(f'La reta de ', numeroUno, ' y ' , numeroDos,' es: ', resta)
print('--------------------MULTIPLICACIÓN-----------------------')
print(f'La multiplicacion de ', numeroUno, ' y ' , numeroDos,' es: ', multiplicacion)
print('--------------------DIVISIÓN-----------------------------')
print(f'La division de ', numeroUno, ' y ' , numeroDos,' es: ', divicion)