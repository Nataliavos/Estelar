#pedir la altura y la base del triangulo, y despues crear la operacion, el area de un triangulo es la multiplicacion
#de su altura y su base, y luego dividirlo por 2, imprimir el resultado

base = int(input("ingresa la base del triangulo "))
altura = int(input("ingresa la altura del triangulo "))

area = base * altura / 2 

print ("el area del triangulo es",area)